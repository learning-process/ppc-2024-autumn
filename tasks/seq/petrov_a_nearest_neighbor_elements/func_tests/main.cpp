#include <gtest/gtest.h>

#include <vector>

#include "seq/petrov_a_nearest_neighbor_elements/include/ops_seq.hpp"

TEST(petrov_a_nearest_neighbor_elements_seq, SUM10) {
 // const int count = 10;

  // Create data
  std::vector<int> in {1,10,3,6,9,2};
  std::vector<int> out(1, 0);

  // Create TaskData
  std::shared_ptr<ppc::core::TaskData> taskDataSeq = std::make_shared<ppc::core::TaskData>();
  taskDataSeq->inputs.emplace_back(reinterpret_cast<uint8_t *>(in.data()));
  taskDataSeq->inputs_count.emplace_back(in.size());
  taskDataSeq->outputs.emplace_back(reinterpret_cast<uint8_t *>(out.data()));
  taskDataSeq->outputs_count.emplace_back(out.size());

  // Create Task
  petrov_a_nearest_neighbor_elements_seq::TestTaskSequential testTaskSequential(taskDataSeq);
  ASSERT_EQ(testTaskSequential.validation(), true);
  testTaskSequential.pre_processing();
  testTaskSequential.run();

  testTaskSequential.post_processing();

  std::vector<int> T{3, 6};
  ASSERT_EQ(T[0], out[0]);
  ASSERT_EQ(T[1], out[1]);
}


